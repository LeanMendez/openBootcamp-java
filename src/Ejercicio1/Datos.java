package Ejercicio1;

public class Datos {
    public static void main(String[] args) {
        //Data type Integer
        byte integerVariable1= 16;
        System.out.println(integerVariable1);
        short integerVariable2 = 34;
        System.out.println(integerVariable2);
        int integerVariable3 = 25000;
        System.out.println(integerVariable3);
        long integerVariable4 = 67L;
        System.out.println(integerVariable4);
        //Data type Decimal
        float decimalVariable1 = 4.55555f;
        System.out.println(decimalVariable1);
        double decimalVariable2 = 3.33333333333333d;
        System.out.println(decimalVariable2);
        //Data type Boolean
        boolean booleanVariable = true;
        System.out.println(booleanVariable);
        //Data type text
        char textVariable1 = 'a';
        System.out.println(textVariable1);
        String textVariable2 = "VAMOS MESSI";
        System.out.println(textVariable2);
    }
}
